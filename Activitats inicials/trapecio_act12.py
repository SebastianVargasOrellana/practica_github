#Pedimos la base y la altura del trapecio al usuario y calculamos su área y perímetro
base=float(input("Introduce el lado del trapecio: "))
altura=float(input("Introduce la altura del trapecio: "))
area=base*altura
perimetro=base*2+altura*2
print("El área del cuadrado es: ",area,"y el perímetro es: ",perimetro)